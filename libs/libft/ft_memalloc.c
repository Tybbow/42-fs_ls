#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*tmp;

	tmp = (void *)malloc(size * sizeof(size_t));
	if (!tmp)
		return (NULL);
	ft_bzero(tmp, size);
	return (tmp);
}
